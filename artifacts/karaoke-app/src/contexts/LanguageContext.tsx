function detectLang(): SupportedLang {
  const stored = localStorage.getItem("myoukee-lang") as SupportedLang | null;
  if (stored && LANGS[stored]) return stored;
  const nav = (navigator.language || "").toLowerCase();
  if (nav.startsWith("ar")) return "ar";
  if (nav.startsWith("ru")) return "ru";
  if (nav.startsWith("es")) return "es";
  if (nav.startsWith("fr")) return "fr";
  if (nav.startsWith("de")) return "de";
  if (nav.startsWith("ja")) return "ja";
  if (nav.startsWith("zh")) return "zh";
  if (nav.startsWith("ko")) return "ko";
  if (nav.startsWith("th")) return "th";
  if (nav.startsWith("vi")) return "vi";
  if (nav.startsWith("tl") || nav.startsWith("fil")) return "tl";
  if (nav.startsWith("id") || nav.startsWith("ms")) return "id";
  if (nav.startsWith("en")) return "en";
  return "he";
}

interface LangContextValue {
  lang: SupportedLang;
  t: Translations;
  setLang: (l: SupportedLang) => void;
  allLangs: { code: SupportedLang; name: string; flag: string }[];
}

const LangCtx = createContext<LangContextValue>({
  lang: "he",
  t: LANGS.he,
  setLang: () => {},
  allLangs: [],
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<SupportedLang>(detectLang);

  const setLang = (l: SupportedLang) => {
    localStorage.setItem("myoukee-lang", l);
    setLangState(l);
    document.documentElement.lang = l;
    document.documentElement.dir = LANGS[l].dir;
  };

  useEffect(() => {
    document.documentElement.lang = lang;
    document.documentElement.dir = LANGS[lang].dir;
  }, [lang]);

  const allLangs = (Object.keys(LANGS) as SupportedLang[]).map(code => ({
    code,
    name: LANGS[code].langName,
    flag: LANGS[code].flag,
  }));

  return (
    <LangCtx.Provider value={{ lang, t: LANGS[lang], setLang, allLangs }}>
      {children}
    </LangCtx.Provider>
  );
}

export function useLang() {
  return useContext(LangCtx);
}
