function detectLang(): SupportedLang {
  const stored = localStorage.getItem("myoukee-lang") as SupportedLang | null;
  if (stored && LANGS[stored]) return stored;
  return "en";
}

interface LangContextValue {
  lang: SupportedLang;
  t: Translations;
  setLang: (l: SupportedLang) => void;
  allLangs: { code: SupportedLang; name: string; flag: string }[];
}

const LangCtx = createContext<LangContextValue>({
  lang: "en",
  t: LANGS.en,
  setLang: () => {},
  allLangs: [],
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<SupportedLang>(detectLang);

  const setLang = (l: SupportedLang) => {
    localStorage.setItem("myoukee-lang", l);
    setLangState(l);
    document.documentElement.lang = l;
    document.documentElement.dir = LANGS[l].dir;
  };

  useEffect(() => {
    document.documentElement.lang = lang;
    document.documentElement.dir = LANGS[lang].dir;
  }, [lang]);

  const allLangs = (Object.keys(LANGS) as SupportedLang[]).map(code => ({
    code,
    name: LANGS[code].langName,
    flag: LANGS[code].flag,
  }));

  return (
    <LangCtx.Provider value={{ lang, t: LANGS[lang], setLang, allLangs }}>
      {children}
    </LangCtx.Provider>
  );
}

export function useLang() {
  return useContext(LangCtx);
}
